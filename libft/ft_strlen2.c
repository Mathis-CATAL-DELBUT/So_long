#include "libft.h"
#include <stdio.h>

size_t	ft_strlen2(const char *str)
{
	size_t	i;
	int		n;

	n = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == 10)
			n = 1;
		i++;
	}
	return (i - n);
}
